class AppDimensions {
  AppDimensions._();

  // --- ESPAÇAMENTOS INTERNOS E EXTERNOS (Padding / Margin) ---

  /// [Uso]: Espaçamento muito pequeno. Usado entre ícones e textos de validação.
  static const double spaceXSmall = 4.0;

  /// [Uso]: Pequenos respiros. Distância entre um ícone e um texto dentro de um botão,
  static const double spaceSmall = 8.0;

  /// [Uso]: Espaçamento médio. Distância padrão entre campos de texto (TextFields)
  static const double spaceMedium = 12.0;

  /// [Uso]: Margem padrão de segurança. Usado no Padding das telas de ponta a ponta
  static const double spaceLarge = 16.0;

  /// [Uso]: Espaçamento largo. Distância horizontal interna de botões compridos,
  static const double spaceXLarge = 24.0;

  /// [Uso]: Espaçamento muito largo. Distância entre elementos distantes, como
  static const double spaceXXLarge = 30.0;

  /// [Uso]: Espaçamento máximo. Usado no final de páginas de rolagem longa
  static const double spaceXXXLarge = 40.0;

  // --- ARREDONDAMENTO DE CANTOS (Border Radius) ---

  /// [Uso]: Arredondamento sutil. Usado em pequenas tags de status (ex: Tag "Urgente").
  static const double radiusSmall = 4.0;

  /// [Uso]: Arredondamento padrão. Aplicado em todas as caixas de texto (TextFields)
  static const double radiusMedium = 8.0;

  /// [Uso]: Arredondamento de seções. Aplicado nos Containers que funcionam como
  static const double radiusLarge = 12.0;

  // --- COMPONENTES E ÍCONES PADRONIZADOS ---

  /// [Uso]: Altura vertical fixa para todos os botões de ação do aplicativo,
  static const double buttonHeight = 48.0;

  /// [Uso]: Tamanho extra pequeno para ícones de status/validação em linha.
  static const double iconSizeXSmall = 14.0;

  /// [Uso]: Tamanho pequeno para ícones de cabeçalho dentro de cards.
  static const double iconSizeSmall = 16.0;

  /// [Uso]: Tamanho padrão para ícones decorativos que acompanham títulos ou botões.
  static const double iconSizeMedium = 20.0;

  /// [Uso]: Tamanho padrão para ícones decorativos que acompanham títulos ou botões.
  static const double iconSize = 24.0;

  /// [Uso]: Tamanho grande para ícones de destaque em cabeçalhos de tela.
  static const double iconSizeLarge = 48.0;
}
